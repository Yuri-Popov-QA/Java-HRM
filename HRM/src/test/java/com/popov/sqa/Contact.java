package com.popov.sqa;

class Contact {
   String firstname;
   String lastname;
   String address;
   String homeNumber;
   String email;

   Contact(String firstname, String lastname, String address, String homeNumber, String email){
      this.firstname=firstname;
      this.lastname=lastname;
      this.address=address;
      this.homeNumber=homeNumber;
      this.email=email;
   }
}
